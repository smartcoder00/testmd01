function LoadMarkDown(markdown_file_path, div_name) {
	var markdown = document.getElementById(div_name)

	fetch(markdown_file_path)
		.then(response => response.blob())
		.then(blob => {
			var r = new FileReader();
			r.onload = function (e) {
				var contents = e.target.result;
				var convertor = new showdown.Converter(),
					text = contents,
					html = convertor.makeHtml(text)

				markdown.innerHTML = html
			}
			r.readAsText(blob);
		});
}
